/*
 * Regression test for a heap buffer overflow when decoding a palette
 * (PHOTOMETRIC_PALETTE) TIFF whose bitsPerSample is less than 8.
 *
 * NSTiffRead expands a palette image to 24-bit RGB, writing three bytes
 * per pixel, while the bitmap buffer was sized from the file's smaller
 * bitsPerSample.  A crafted 4-bit palette image therefore overran the
 * destination buffer.  The two fixtures above were produced with libtiff:
 *   badPalette4bppTIFF  - 1024x1024, PHOTOMETRIC_PALETTE, bitsPerSample 4,
 *                         LZW-compressed uniform data: small on disk but
 *                         drives a multi-megabyte over-write when decoded.
 *   goodPalette8bppTIFF - 8x8, PHOTOMETRIC_PALETTE, bitsPerSample 8,
 *                         a valid palette image used as a positive control.
 */
int main()
{
  NSAutoreleasePool *arp = [NSAutoreleasePool new];
  NSData            *data;
  NSBitmapImageRep  *rep;

  data = [NSData dataWithBytes: badPalette4bppTIFF
                        length: sizeof(badPalette4bppTIFF)];
  rep = [[NSBitmapImageRep alloc] initWithData: data];
  PASS(rep == nil,
    "a palette TIFF with bitsPerSample < 8 is rejected, not decoded with a buffer overflow");
  [rep release];

  data = [NSData dataWithBytes: goodPalette8bppTIFF
                        length: sizeof(goodPalette8bppTIFF)];
  rep = [[NSBitmapImageRep alloc] initWithData: data];
  PASS(rep != nil && [rep pixelsWide] == 8 && [rep pixelsHigh] == 8,
    "a valid 8-bit palette TIFF still decodes");
  [rep release];

  [arp release];
  return 0;
}
