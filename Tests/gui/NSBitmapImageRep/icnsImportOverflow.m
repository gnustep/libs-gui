/*
 * Regression test for a heap buffer overflow when importing an ICNS icon
 * whose icns family resource size field is invalid.
 *
 * icns_import_family_data() read the resource size straight from the file
 * and used it for malloc() and a memcpy() of (size - 8) bytes without any
 * bounds check.  A value below 8 made (size - 8) underflow to a huge
 * length, and a value larger than the file made the memcpy read past the
 * end of the input.  The two fixtures below are built by hand:
 *   badICNSunderflow - "icns" magic with a family resource size of 4
 *                      (< the 8 byte header), which triggered the overflow.
 *   goodICNS32       - a valid 32x32 il32 icon, used as a positive control.
 */
int main()
{
  NSAutoreleasePool *arp = [NSAutoreleasePool new];
  NSData            *data;
  NSBitmapImageRep  *rep;

  data = [NSData dataWithBytes: badICNSunderflow
                        length: sizeof(badICNSunderflow)];
  rep = [[NSBitmapImageRep alloc] initWithData: data];
  pass(rep == nil,
    "an ICNS file with an undersized family resource size is rejected, not decoded with a buffer overflow");
  [rep release];

  data = [NSData dataWithBytes: goodICNS32 length: sizeof(goodICNS32)];
  rep = [[NSBitmapImageRep alloc] initWithData: data];
  pass(rep != nil && [rep pixelsWide] == 32 && [rep pixelsHigh] == 32,
    "a valid 32x32 ICNS icon still decodes");
  [rep release];

  [arp release];
  return 0;
}
