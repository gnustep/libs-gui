/*
 * Regression test for out-of-bounds reads when decoding an ICNS icon whose
 * 8-bit mask element (or RLE icon data) is shorter than the icon dimensions.
 *
 * icns_get_image32_with_mask_from_family() copied imageHeight*imageWidth mask
 * bytes (and, for RLE icons, run bytes) from the element without checking
 * against the end of the element data, reading past the heap allocation when
 * the element was truncated.  The over-read is silent on a normal heap (it is
 * proved with AddressSanitizer); this test checks that a truncated-mask icon
 * still decodes its (valid) colour data and does not crash.
 *
 * truncatedMaskICNS: a 32x32 il32 icon filled with a solid colour
 * (R=90, G=140, B=200) followed by an l8mk mask element with no data.
 *
 * This over-read is in the built-in GNUstep ICNS reader, which is used only
 * when libs-gui is built without the system libicns library.  A truncated
 * mask is rejected outright by libicns, so the test below is compiled only
 * when that library is absent.  libs-gui enables libicns exactly when its
 * <icns.h> header is available at build time, so we use the same condition
 * here (config.h, which defines HAVE_LIBICNS, is not installed for tests).
 */
#if defined(__has_include)
#  if __has_include(<icns.h>)
#    define GS_TEST_SYSTEM_ICNS 1
#  endif
#endif
int main()
{
  NSAutoreleasePool *arp = [NSAutoreleasePool new];
#if !defined(GS_TEST_SYSTEM_ICNS)
  NSData            *data;
  NSBitmapImageRep  *rep;
  const unsigned char *px;

  data = [NSData dataWithBytes: truncatedMaskICNS
                        length: sizeof(truncatedMaskICNS)];
  rep = [[NSBitmapImageRep alloc] initWithData: data];
  pass(rep != nil && [rep pixelsWide] == 32 && [rep pixelsHigh] == 32,
    "a 32x32 ICNS icon with a truncated mask decodes without a buffer overflow");

  px = [rep bitmapData];
  pass(px != NULL && px[0] == 90 && px[1] == 140 && px[2] == 200,
    "the icon colour data is decoded correctly");

  [rep release];
#endif
  [arp release];
  return 0;
}
