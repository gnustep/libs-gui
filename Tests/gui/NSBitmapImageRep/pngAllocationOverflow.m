#import "ObjectTesting.h"
#import <Foundation/NSData.h>
#import <Foundation/NSAutoreleasePool.h>
#import <AppKit/NSBitmapImageRep.h>

/*
 * Regression test for an integer overflow in the PNG image-buffer allocation.
 *
 * -_initBitmapFromPNG: computed the decoded-image buffer size as
 * bytes_per_row * height using int arithmetic, and used a stack VLA
 * (row_pointers[height]) sized from the header.  A large image (libpng allows
 * dimensions up to 1000000) made bytes_per_row * height wrap to a small or
 * negative value, so the allocation was far too small (or NULL, unchecked),
 * and png_read_image() then wrote the decoded rows past it.
 *
 * overflowPNG: a 2148 x 1000000 8-bit grayscale PNG.  bytes_per_row (2148) *
 * height (1000000) = 2148000000 overflows a 32-bit int.  A safe decoder must
 * reject it rather than crash.
 * validPNG: a 4x4 RGB image filled with (R=90, G=140, B=200), positive control.
 */
static const unsigned char overflowPNG[] = {
  0x89,0x50,0x4e,0x47,0x0d,0x0a,0x1a,0x0a,0x00,0x00,0x00,0x0d,
  0x49,0x48,0x44,0x52,0x00,0x00,0x08,0x64,0x00,0x0f,0x42,0x40,
  0x08,0x00,0x00,0x00,0x00,0x79,0xc3,0x1a,0x11,0x00,0x00,0x00,
  0x28,0x49,0x44,0x41,0x54,0x78,0xda,0xed,0xd6,0x31,0x01,0x00,
  0x00,0x0c,0xc2,0x30,0xa4,0x23,0x1d,0x15,0xbb,0x96,0x48,0xe8,
  0xd5,0x14,0x00,0xe0,0x40,0x24,0x00,0x00,0x4c,0x06,0x00,0x60,
  0x32,0x00,0x80,0xdf,0x06,0x74,0xa6,0xc8,0xf1,0xa3,0xca,0x34,
  0x53,0x00,0x00,0x00,0x00,0x49,0x45,0x4e,0x44,0xae,0x42,0x60,
  0x82,
};

static const unsigned char validPNG[] = {
  0x89,0x50,0x4e,0x47,0x0d,0x0a,0x1a,0x0a,0x00,0x00,0x00,0x0d,
  0x49,0x48,0x44,0x52,0x00,0x00,0x00,0x04,0x00,0x00,0x00,0x04,
  0x08,0x02,0x00,0x00,0x00,0x26,0x93,0x09,0x29,0x00,0x00,0x00,
  0x10,0x49,0x44,0x41,0x54,0x78,0xda,0x63,0x88,0xea,0x39,0x01,
  0x47,0x0c,0xc4,0x71,0x00,0xb4,0x32,0x1a,0xe1,0xd2,0x68,0x18,
  0x76,0x00,0x00,0x00,0x00,0x49,0x45,0x4e,0x44,0xae,0x42,0x60,
  0x82,
};

int main()
{
  NSAutoreleasePool *arp = [NSAutoreleasePool new];
  NSData            *data;
  NSBitmapImageRep  *rep;
  const unsigned char *px;

  data = [NSData dataWithBytes: overflowPNG length: sizeof(overflowPNG)];
  rep = [[NSBitmapImageRep alloc] initWithData: data];
  pass(rep == nil,
    "a PNG whose pixel-buffer size overflows is rejected, not decoded with a buffer overflow");
  [rep release];

  data = [NSData dataWithBytes: validPNG length: sizeof(validPNG)];
  rep = [[NSBitmapImageRep alloc] initWithData: data];
  pass(rep != nil && [rep pixelsWide] == 4 && [rep pixelsHigh] == 4,
    "a valid PNG still decodes");
  px = [rep bitmapData];
  pass(rep == nil || (px != NULL && px[0] == 90 && px[1] == 140 && px[2] == 200),
    "the valid PNG colour data is decoded correctly");
  [rep release];

  [arp release];
  return 0;
}
